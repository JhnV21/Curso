numero_1 = int(input('Digite um valor: '))
numero_2 = int(input('Digite outro valor: '))
soma = numero_1 + numero_2
multiplicacao = numero_1 * numero_2
divisao = numero_1 / numero_2
divisao_inteira = numero_1 // numero_2
exponenciacao = numero_1 ** numero_2
print(f'A soma é {soma}, o produto é {multiplicacao} e a divisão é {divisao:.2f}')
print(f'Divisão inteira {divisao_inteira} e potência {exponenciacao}')

