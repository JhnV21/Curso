'''
Crie um algoritmo que leia
um número e mostre o seu dobro,
triplo e raiz quadrada.
'''

numero = int(input('Digite um número: '))
numero_dobro = numero * 2
numero_triplo = numero * 3
numero_raiz_quadrada = numero ** 0.5

print(f'Voce Digitou {numero}, o dobro dele é {numero_dobro}, o triplo é {numero_triplo} e a raiz quadrada é {numero_raiz_quadrada:.2f}')
